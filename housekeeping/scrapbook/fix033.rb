replace260.each do |id|
    s  = Source.find(id)

    s.marc.load_source true
    t = s.marc.first_occurance("033")
    next if !t

    # Count up the 260s
    count260 = s.marc.by_tags("260").count

    if count260 == 0
        new_tag = t.deep_copy
        new_tag.tag = "260"
        s.marc.root.children.insert(s.marc.get_insert_position(new_tag.tag), new_tag)
        #ap s.marc
    else
        t260 = s.marc.first_occurance("260")
        t260c = t260.fetch_first_by_tag("c")

        date = t.fetch_first_by_tag("a").content

        if t260c && t260c.content
            st = t260.fetch_first_by_tag("c")
            st.destroy_yourself if st

            t260.add_at(MarcNode.new("source", "c", date, nil), 0)
            t260.sort_alphabetically

            #ap s.marc
        else
            t260.add_at(MarcNode.new("source", "c", date, nil), 0)
            t260.sort_alphabetically
            #ap s.marc
        end
    end

    s.marc.by_tags("033").each {|t| t.destroy_yourself}
    #ap s.marc
    s.save
end


